function getRates(url)
	dt = DataFrame(CSV.File(Downloads.download(url); dateformat="mm/dd/yyyy") )
	dt[:,:Date] = dt[:,:Date] .+ Dates.Year(2000)
	dropmissing!(dt)
	for e in names(dt)
		if e != "Date"
			dt[:,e] = convert.(Float64,dt[:,e])
		end
	end
	rates = TimeArray(dt,timestamp = :Date)
end

