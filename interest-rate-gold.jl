function genMatrix(data,lookback,ncol,len)
	n = 1
	full_matrix = zeros(Float64,(sz[1]-lookback,ncol*lookback+1))
	for i in lookback:len-1
		m = 1
		for k in 1:ncol
			for j in 1:lookback
				full_matrix[n,m] = data[i-j+1,k]
				m = m + 1
			end
		end
		full_matrix[n,m] = data[i+1,ncol+1]
		n = n + 1
	end
	return full_matrix
end

